CREATE TABLE users (
    user_id INT PRIMARY KEY,
    user_name VARCHAR(255)
);
INSERT INTO users (user_id, user_name)
VALUES (1, 'Alice'),
    (2, 'Bob'),
    (3, 'Charlie');
-- ビューを作成
CREATE VIEW user_view AS
SELECT user_id AS "ユーザーID",
    user_name AS "名前"
FROM users;
